#include "../../libft.h"

char	*cut_next_line(char *line)
{
	int	i;

	i = 0;
	while (line[i] != '\0')
	{
		if (ft_isnextline(line[i]))
			line[i] = '\0';
		i++;
	}
	return (line);
}
